# compare 

x = float(input("Enter any number, x: "))
y = float(input("Enter any number, y: "))       

if x < y:
    print("x is less than y")
elif x > y:
    print("x is greater than y")
else:
    print("x is equal to y")    

# we can use elif to compare multiple conditions and improve the efficiency of our code
# elif will only execute if the previous condition is false   

# we can also use else to execute code if all previous conditions are false, simplifying yet another question being asked

